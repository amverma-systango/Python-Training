# 6) Python Operators

## Arithmetic operator

# addition
var_1 = 200 + 10000
print(var_1)

# subtraction
var_2 = 20000 - 500
print(var_2)

# multiplicaton
var_3 = 800 * 2
print(var_3)

# division
var_4 = 800 / 2
print(var_4)

# modulo(remainder)
var_5 = 900 % 7
print(var_5)

# integer division
var_6 = 777//4
print(var_6)

# exponent
var_7 = 2**4
print(var_7)




